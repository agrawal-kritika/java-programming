/* Data types, variables and its naming conventions */

class variables {
    public static void main(String[] args) {
        String name = "Monica";
        int age = 24;
        char gender = 'F';
        long phno = 1234567890l; // long literals should be suffixed with L
        float marks = 99.8f; // float literals should be suffixed with f
        double salary = 80000.234567;

        // printing the values
        System.out.println("Name: " + name); // To print the value, it should be given with + symbol
        System.out.println("age: " + age);
        System.out.println("Gender: " + gender);
        System.out.println("Phone no.: " + phno);
        System.out.println("marks: " + marks);
        System.out.println("Salary: " + salary);
    }
}
